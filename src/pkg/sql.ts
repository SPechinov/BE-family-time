/**
 * Нормализует SQL-запрос для логирования:
 * - Удаляет лишние пробелы
 * - Заменяет множественные переносы строк на один пробел
 * - Убирает ведущие/ведущие пробелы
 *
 * Безопасно: используется только для логирования, не влияет на выполнение запроса
 *
 * @example
 * ```sql
 * `
 *   SELECT *
 *   FROM users
 *   WHERE id = $1
 * `
 * ```
 * → `"SELECT * FROM users WHERE id = $1"`
 */
export const normalizeQuery = (query: string): string => {
  return query
    .replace(/\s+/g, ' ') // Заменяем все пробельные символы (включая \n, \t) на один пробел
    .trim() // Убираем пробелы по краям
    .replace(/\s*([(),=])\s*/g, '$1'); // Убираем пробелы вокруг скобок и запятых
};
